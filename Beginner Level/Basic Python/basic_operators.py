#Arithmetic
x = 5
y = 2

# Addition
print(x + y) # Output: 7

# Subtraction
print(x - y) # Output: 3

# Multiplication
print(x * y) # Output: 10

# Division
print(x / y) # Output: 2.5

# Modulus
print(x % y) # Output: 1

# Exponentiation
print(x ** y) # Output: 25

# Floor division
print(x // y) # Output: 2

#Comparison
x = 5
y = 2
is_equal = x == y
is_not_equal = x != y
is_greater_than = x > y
is_less_than = x < y
is_greater_than_or_equal = x >= y
is_less_than_or_equal = x <= y

print(is_equal) # Output: False
print(is_not_equal) # Output: True
print(is_greater_than) # Output: True
print(is_less_than) # Output: False
print(is_greater_than_or_equal) # Output: True
print(is_less_than_or_equal) # Output: False

#Logical
x = True
y = False

# AND
print(x and y) # Output: False

# OR
print(x or y) # Output: True

# NOT
print(not x) # Output: False
print(not y) # Output: True

#Identity
x = 5
y = 5

is_same = x is y
is_not_same = x is not y

print(is_same) # Output: True
print(is_not_same) # Output: False

